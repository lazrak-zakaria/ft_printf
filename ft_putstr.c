#include "ft_printf.h"

void	ft_putstr(char *s, int *l)
{
	if (!s)
	{
		if (write(1, "(null)", 6) < 0)
			*l = -1;
		else
			*l += 6;
		return ;
	}
	while (*s)
	{
		ft_putchar(*s, l);
		if (*l < 0)
			return ;
		s++;
	}
}
