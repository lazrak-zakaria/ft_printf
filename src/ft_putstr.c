#include "../ft_printf.h"

void	ft_putstr(char *s, int *l)
{
	if (!s)
	{
		*l += write(1,"(null)",6);
		return ;
	}
	while (*s)
	{
		ft_putchar(*s, l);
		s++;
	}
}
